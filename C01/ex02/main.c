#include <unistd.h>

void	ft_swap(int *a, int *b);

int	main(void)
{
	int	a;
	int	b;

	a = 48;
	b = 57;
	ft_swap(&a, &b);
	write(1, &a, 1);
	write(1, &b, 1);
}
